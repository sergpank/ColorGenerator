public class Steps {

    private double redInc;
    private double greenInc;
    private double blueInc;
    private double redDec;
    private double greenDec;
    private double blueDec;


    double getRedInc() {
        return redInc;
    }


    Steps setRedInc(double aRedInc) {
        redInc = aRedInc;
        return this;
    }


    double getGreenInc() {
        return greenInc;
    }


    Steps setGreenInc(double aGreenInc) {
        greenInc = aGreenInc;
        return this;
    }


    double getBlueInc() {
        return blueInc;
    }


    Steps setBlueInc(double aBlueInc) {
        blueInc = aBlueInc;
        return this;
    }


    double getRedDec() {
        return redDec;
    }


    Steps setRedDec(double aRedDec) {
        redDec = aRedDec;
        return this;
    }


    double getGreenDec() {
        return greenDec;
    }


    Steps setGreenDec(double aGreenDec) {
        greenDec = aGreenDec;
        return this;
    }


    double getBlueDec() {
        return blueDec;
    }


    Steps setBlueDec(double aBlueDec) {
        blueDec = aBlueDec;
        return this;
    }
}